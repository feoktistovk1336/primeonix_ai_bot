from services.ai import ask_ai


async def analyze_brand_voice(samples: str):
    result = await ask_ai(f"""
Ты — эксперт по tone of voice, SMM и авторскому стилю.

Проанализируй примеры постов автора:

{samples}

Составь Brand Voice Profile.

Структура:

Стиль автора:
Тон:
Длина фраз:
Длина абзацев:
Эмоциональность:
Уровень экспертности:
Как автор начинает посты:
Как автор объясняет мысли:
Какие CTA использует:
Какие слова и обороты повторяются:
Что нельзя менять в стиле:
Как писать новые посты в этом стиле:

Правила:
- без markdown
- без звёздочек
- максимально конкретно
""")

    return result.strip()