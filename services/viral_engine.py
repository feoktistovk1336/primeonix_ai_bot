VIRAL_HOOKS = [
    "Ты теряешь деньги не потому что плохо продаёшь, а потому что...",
    "90% людей делают эту ошибку и даже не замечают",
    "Если бы я начинал заново, я бы сделал вот что",
    "Вот почему у тебя нет результата, хотя ты стараешься",
    "Это выглядит просто, но именно здесь большинство сливается",
    "Ты можешь игнорировать это, но оно уже влияет на твои продажи",
    "Главная причина, почему твой контент не работает",
    "Пока ты думаешь, другие уже зарабатывают на этом",
    "Этот простой шаг может изменить весь результат",
    "Не делай это, если хочешь расти быстрее"
]


VIRAL_ANGLES = [
    "ошибка",
    "потеря денег",
    "страх упустить возможность",
    "быстрый инсайт",
    "личный опыт",
    "разбор мифа",
    "контраст до/после",
    "провокация",
    "секрет",
    "простая инструкция"
]


def build_viral_block(topic: str):
    hooks_text = "\n".join([f"- {hook}" for hook in VIRAL_HOOKS])
    angles_text = "\n".join([f"- {angle}" for angle in VIRAL_ANGLES])

    return f"""
Viral Engine:

Тема:
{topic}

Используй эти модели хуков:
{hooks_text}

Используй эти углы подачи:
{angles_text}

Правила:
- первый абзац должен цеплять
- не начинай с банальностей
- не пиши "в современном мире"
- добавляй конфликт, боль или интригу
- делай текст живым и энергичным
"""