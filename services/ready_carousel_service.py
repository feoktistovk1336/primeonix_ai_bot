from services.ai import ask_ai


async def generate_ready_carousel(
    topic: str,
    style_block="",
    profile_block="",
    memory_block="",
    content_goal=""
):
    result = await ask_ai(f"""
{style_block}

Используй Brand Brain автора:

{profile_block}

{memory_block}

Ты — premium SMM-стратег и carousel copywriter.

Создай карусель на тему:
{topic}

Цель:
{content_goal}

Нужно ровно 5 слайдов.

Формат строго такой:

Слайд 1:
Заголовок: максимум 6 слов
Текст: максимум 12 слов

Слайд 2:
Заголовок: максимум 5 слов
Текст: максимум 12 слов

Слайд 3:
Заголовок: максимум 5 слов
Текст: максимум 12 слов

Слайд 4:
Заголовок: максимум 5 слов
Текст: максимум 12 слов

Слайд 5:
Заголовок: максимум 5 слов
Текст: максимум 10 слов

Правила:
- не пиши длинные абзацы
- не используй markdown
- не используй звёздочки
- не используй таблицы
- каждый слайд должен быть очень коротким
- стиль: дорогой, уверенный, экспертный
- текст должен легко помещаться на картинку
""")

    return result.strip()